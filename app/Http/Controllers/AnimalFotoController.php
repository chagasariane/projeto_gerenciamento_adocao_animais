<?php

namespace App\Http\Controllers;

use App\Http\Requests\StoreAnimalFotoRequest;

use App\Models\Animal;
use App\Models\AnimalFoto;

use Illuminate\Support\Facades\Storage;

class AnimalFotoController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | STORE
    |--------------------------------------------------------------------------
    */

    public function store(
        StoreAnimalFotoRequest $request,
        string $animalId
    ) {
        /*
        |--------------------------------------------------------------------------
        | ANIMAL
        |--------------------------------------------------------------------------
        */

        $animal = Animal::findOrFail($animalId);

        /*
        |--------------------------------------------------------------------------
        | AUTHORIZATION
        |--------------------------------------------------------------------------
        */

        $this->authorize('update', $animal);

        /*
        |--------------------------------------------------------------------------
        | FOTO PRINCIPAL
        |--------------------------------------------------------------------------
        */

        $possuiFotoPrincipal =
            $animal->fotoPrincipal()->exists();

        /*
        |--------------------------------------------------------------------------
        | UPLOAD
        |--------------------------------------------------------------------------
        */

        foreach (
            $request->file('fotos')
            as $index => $foto
        ) {

            /*
            |--------------------------------------------------------------------------
            | SALVA ARQUIVO
            |--------------------------------------------------------------------------
            */

            $caminho = $foto->store(
                'animais',
                'public'
            );

            /*
            |--------------------------------------------------------------------------
            | DEFINE PRINCIPAL
            |--------------------------------------------------------------------------
            */

            $principal = false;

            if (
                !$possuiFotoPrincipal &&
                $index == 0
            ) {

                $principal = true;
            }

            /*
            |--------------------------------------------------------------------------
            | REGISTRO
            |--------------------------------------------------------------------------
            */

            AnimalFoto::create([

                'animal_id' => $animal->id,

                'caminho' => $caminho,

                'principal' => $principal
            ]);
        }

        return back()->with(
            'success',
            'Fotos adicionadas com sucesso!'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | DESTROY
    |--------------------------------------------------------------------------
    */

    public function destroy(string $id)
    {
        /*
        |--------------------------------------------------------------------------
        | FOTO
        |--------------------------------------------------------------------------
        */

        $foto = AnimalFoto::with('animal')
            ->findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | AUTHORIZATION
        |--------------------------------------------------------------------------
        */

        $this->authorize(
            'update',
            $foto->animal
        );

        /*
        |--------------------------------------------------------------------------
        | REMOVE ARQUIVO
        |--------------------------------------------------------------------------
        */

        if (
            Storage::disk('public')
                ->exists($foto->caminho)
        ) {

            Storage::disk('public')
                ->delete($foto->caminho);
        }

        /*
        |--------------------------------------------------------------------------
        | REMOVE REGISTRO
        |--------------------------------------------------------------------------
        */

        $foto->delete();

        /*
        |--------------------------------------------------------------------------
        | REDEFINE FOTO PRINCIPAL
        |--------------------------------------------------------------------------
        */

        if (
            $foto->principal &&
            $foto->animal
                ->fotos()
                ->exists()
        ) {

            $novaPrincipal =
                $foto->animal
                    ->fotos()
                    ->first();

            $novaPrincipal->update([
                'principal' => true
            ]);
        }

        return back()->with(
            'success',
            'Foto removida com sucesso!'
        );
    }
}